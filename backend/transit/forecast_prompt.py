def _sphere_highlights(transit_events: list[dict], houses: list[dict]) -> list[dict]:
    """Определить активные жизненные сферы по транзитам через дома натальной карты.

    Знак транзитной планеты → дом натальной карты (по куспиду) → сфера.
    """
    if not houses:
        return []

    # Строим маппинг знак → номер дома
    sign_to_house: dict[str, int] = {}
    for h in houses:
        sign = h.get("sign") or h.get("cusp_sign")
        num  = h.get("number") or h.get("num")
        if sign and num:
            sign_to_house[str(sign)] = int(num)

    sphere_hits: dict[int, dict] = {}

    for ev in transit_events:
        t_sign   = ev.get("transit_sign") or ev.get("sign")
        t_planet = ev.get("transit_planet") or ev.get("planet")
        if not t_sign or not t_planet:
            continue

        house_num = sign_to_house.get(t_sign)
        if not house_num or house_num not in HOUSE_SPHERE_MAP:
            continue

        is_slow  = t_planet in SLOW_PLANETS
        priority = "main" if is_slow else "active"

        # Конкретное значение транзита из методички
        planet_meaning = (
            PLANET_HOUSE_MEANINGS.get(t_planet, {}).get(house_num, "")
        )

        if house_num not in sphere_hits:
            sphere_hits[house_num] = {
                "house":          house_num,
                "sphere_name":    HOUSE_SPHERE_MAP[house_num]["name"],
                "emoji":          HOUSE_SPHERE_MAP[house_num]["emoji"],
                "hint":           HOUSE_SPHERE_MAP[house_num]["hint"],
                "priority":       priority,
                "planets":        [],
                "planet_meanings": [],
            }

        if t_planet not in sphere_hits[house_num]["planets"]:
            sphere_hits[house_num]["planets"].append(t_planet)
        if planet_meaning and planet_meaning not in sphere_hits[house_num]["planet_meanings"]:
            sphere_hits[house_num]["planet_meanings"].append(planet_meaning)
        if is_slow:
            sphere_hits[house_num]["priority"] = "main"

    result = sorted(
        sphere_hits.values(),
        key=lambda x: (0 if x["priority"] == "main" else 1, x["house"]),
    )
    return result[:7]


def _compact_natal(natal_profile: dict) -> str:
    """Компактное текстовое представление натальной карты для промпта."""
    lines = []
    planets = natal_profile.get("planets", [])
    if planets:
        lines.append("Планеты:")
        for p in planets:
            name  = p.get("name", "?")
            sign  = p.get("sign", "?")
            house = p.get("house")
            retro = " ℞" if p.get("retrograde") else ""
            h_str = f" (дом {house})" if house else ""
            lines.append(f"  {name}: {sign}{h_str}{retro}")

    houses = natal_profile.get("houses", [])
    if houses:
        lines.append("Куспиды домов:")
        for h in houses[:12]:
            num  = h.get("number") or h.get("num", "?")
            sign = h.get("sign", "?")
            lines.append(f"  Дом {num}: {sign}")

    asc = natal_profile.get("ascendant")
    if asc:
        deg = asc.get("degree", 0)
        lines.append(f"Асцендент: {asc.get('sign','?')} {float(deg):.1f}°")
    mc = natal_profile.get("midheaven")
    if mc:
        deg = mc.get("degree", 0)
        lines.append(f"MC: {mc.get('sign','?')} {float(deg):.1f}°")

    return "\n".join(lines) if lines else "Профиль недоступен"


