# ПАРСЕР ОТВЕТА AI
# ══════════════════════════════════════════════════════════════════════════════

def parse_forecast_response(raw: str) -> dict:
    """Распарсить JSON из ответа AI. Убирает markdown-обёртку если есть."""
    text = raw.strip()
    if text.startswith("```"):
        lines = text.split("\n")
        end = -1 if lines[-1].strip() == "```" else len(lines)
        text = "\n".join(lines[1:end])
    return json.loads(text.strip())
